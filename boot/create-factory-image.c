/* This is the standard Gary S. Brown's 32 bit CRC algorithm, but
   accumulate the CRC into the result of a previous CRC. */
cyg_uint32 crc32_accumulate(cyg_uint32 crc32val, unsigned char *s, int len)
{
    int i;
    for (i = 0;  i < len;  i++)
    {
        crc32val = crc32_tab[(crc32val ^ s[i]) & 0xff] ^ (crc32val >> 8);
    }

    return crc32val;
}

cyg_uint32 get_file_crc32(const char* file)
{
	FILE* fp = fopen(file, "rb");
	if (fp == NULL)
	{
		perror("fopen");
		return 0;
	}

	char read_buf[BUFLEN];
	cyg_uint32 crc = 0;
	while (!feof(fp))
	{
		size_t bytes_read = fread(read_buf, 1, BUFLEN, fp);
        crc = crc32_accumulate(crc, read_buf, bytes_read);
	}

	fclose(fp);
	return crc;
}

struct fis_image_desc
{
	unsigned char   name[16];      // Null terminated name
	cyg_uint32      flash_base;    // Address within FLASH of image
	cyg_uint32      mem_base;      // Address in memory where it executes
	unsigned long   size;          // Length of image
	cyg_uint32      entry_point;   // Execution entry point
	unsigned long   data_length;   // Length of actual data
	unsigned char   _pad[256-FIS_IMAGE_DESC_SIZE_UNPADDED];
	unsigned long   desc_cksum;    // Checksum over image descriptor
	unsigned long   file_cksum;    // Checksum over image data
};

static int init_fis(struct fis_image_desc* fis_p,
	const char* boot_splash_path,
	const char* kernel_path,
	const char* initramfs_path)
{
	struct stat stat_buf;
	struct fis_image_desc* p = fis_p;
	memset(p, 0, NUM_REDBOOT_PARTS * sizeof(struct fis_image_desc));

	// Create RedBoot partition
	strcpy(p->name, "RedBoot");
	p->size = 0x40000;
	p++;

	// Create RedBoot config partition
	strcpy(p->name, "RedBoot config");
	p->flash_base = 0x9F000;
	p->mem_base = 0x9F000;
	p->size = REDBOOT_CFG_SIZE;
	p++;

	// Create FIS directory partition
	strcpy(p->name, "FIS directory");
	p->flash_base = 0x80000;
	p->mem_base = 0x80000;
	p->size = 0x1F000;
	p++;

	// Create boot splash partition
	if (0 != stat(boot_splash_path, &stat_buf))
	{
		perror("stat");
		return -1;
	}
	strcpy(p->name, "boot splash");
	p->flash_base = 0x100000;
	p->mem_base = 0x100000;
	p->size = 0x100000;
	p->entry_point = 0x100000;
	p->data_length = stat_buf.st_size;
	p->file_cksum = get_file_crc32(boot_splash_path);
	p++;

	// Create kernel partition
	if (0 != stat(kernel_path, &stat_buf))
	{
		perror("stat");
		return -1;
	}
	strcpy(p->name, "kernel");
	p->flash_base = 0x200000;
	p->mem_base = 0x100000;
	p->size = 0x200000;
	p->entry_point = 0x100000;
	p->data_length = stat_buf.st_size;
	p->file_cksum = get_file_crc32(kernel_path);
	p++;

	// Create initramfs partition
	if (0 != stat(initramfs_path, &stat_buf))
	{
		perror("stat");
		return -1;
	}
	strcpy(p->name, "initramfs");
	p->flash_base = 0x400000;
	p->mem_base = 0x100000;
	p->size = 0x300000;
	p->entry_point = 0x100000;
	p->data_length = stat_buf.st_size;
	p->file_cksum = get_file_crc32(initramfs_path);

	return 0;
}

static int write_image(const char* image_path, int partition_size, FILE* ofp)
{
	FILE* fp = fopen(image_path, "rb");
	if (fp == NULL)
	{
		perror("fopen");
		return -1;
	}

	unsigned char buf[BUFLEN];
	int bytes_read = 0, total_bytes_written = 0;
	while (!feof(fp))
	{
		bytes_read = fread(buf, 1, BUFLEN, fp);
		fwrite(buf, 1, bytes_read, ofp);
		total_bytes_written += bytes_read;
	}

	if (total_bytes_written < partition_size)
	{
		int bytes_left = partition_size - total_bytes_written;
		unsigned char* pad_buf = (unsigned char *)malloc(bytes_left);
		memset(pad_buf, 0xFF, bytes_left);
		fwrite(pad_buf, 1, bytes_left, ofp);
		free(pad_buf);
	}

	fclose(fp);
}

int main(int argc, char* argv[])
{
	if (argc != 7)
	{
		fprintf(stderr, "Usage: %s <redboot.bin> <boot-splash> <zImage> <zImage-initramfs> <rootfs.yaffs2> <output image>\n", argv[0]);
		return -1;
	}

	FILE* ofp = fopen(argv[6], "wb");
	if (ofp == NULL)
	{
		perror("fopen");
		return -1;
	}

	// Write redboot
	if (write_image(argv[1], REDBOOT_SIZE, ofp) == -1)
	{
		fprintf(stderr, "Failed to write redboot to factory image!\n");
		goto Error;
	}

	// Write FIS
	unsigned char fis[FIS_SIZE];
	memset(fis, 0xFF, FIS_SIZE);
	if (init_fis((struct fis_image_desc *)fis, argv[2], argv[3], argv[4]) == -1)
	{
		fprintf(stderr, "Can't initialize fis partition.\n");
		goto Error;
	}
	memcpy(fis + 0x1F000, redboot_cfg, REDBOOT_CFG_SIZE);
	fwrite(fis, 1, FIS_SIZE, ofp);

	// Write boot splash
	if (write_image(argv[2], BOOTSPLASH_SIZE, ofp) == -1)
	{
		fprintf(stderr, "Failed to write boot splash to factory image!\n");
		goto Error;
	}

	// Write zImage
	if (write_image(argv[3], KERNEL_SIZE, ofp) == -1)
	{
		fprintf(stderr, "Failed to write zImage to factory image!\n");
		goto Error;
	}

	// Write zImage-initramfs
	if (write_image(argv[4], INITRAMFS_SIZE, ofp) == -1)
	{
		fprintf(stderr, "Failed to write zImage-initramfs to factory image!\n");
		goto Error;
	}

	// Get file size of rootfs image
	struct stat stat_buf;
	if (0 != stat(argv[5], &stat_buf))
	{
		perror("stat");
		goto Error;
	}

	// Write rootfs.yaffs2
	if (write_image(argv[5], stat_buf.st_size, ofp) == -1)
	{
		fprintf(stderr, "Failed to write rootfs to factory image!\n");
		goto Error;
	}

	fclose(ofp);
	return 0;

Error:
	fclose(ofp);
	return 1;
}
